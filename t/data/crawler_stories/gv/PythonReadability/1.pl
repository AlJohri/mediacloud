 All over Brazil, mobilization against the proposed bill has been organized by a group of bloggers, who have put together a manifesto, a  Twitter account  and  Mega N\x{e3}o!  [Mega No!, pt], a blog created with the single purpose of gathering information and contributing to clarify what this bill might mean for Internet users in the country: 




 

